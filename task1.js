
let monts = Number(prompt("1-ден 12-ге дейін сан енгізіңіз:"))

switch (monts) {
    case 3:
        alert("Ай 3,Маусым: Spring")
        break;
        
        case 4:
            alert("Ай 4,Сәуір: Spring")
            break;
        
            case 5:
                alert("Ай 5,Наурыз: Spring")
                break;


                case 6:
                    alert("Ай 6,Мамыр: Summer")
                    break;
                    
                    case 7:
                        alert("Ай 7,Шілде: Summer")
                        break;
                    
                        case 8:
                            alert("Ай 8,Тамыз: Summer")
                            break;

                            
                case 9:
                    alert("Ай 9,Қыркүйек: Autumn")
                    break;
                    
                    case 10:
                        alert("Ай 10,Қазан: Autumn")
                        break;
                    
                        case 11:
                            alert("Ай 11,Қараша: Autumn")
                            break;



                            case 12:
                                alert("Ай 12,Желтоқсан: Winter")
                                break;
                                
                                case 1:
                                    alert("Ай 1,Қаңтар: Winter")
                                    break;
                                
                                    case 2:
                                        alert("Ай 2,Ақпан: Winter")
                                        break;
            

                            
                
                

    default: 
      alert("1 ден 12ге дейін сан еңгіз")
        break;
}